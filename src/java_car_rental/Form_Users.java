    private void Table_UsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_UsersMouseClicked
        // get the selected brand
        
        int index = Table_Users.getSelectedRow();
        int id = Integer.valueOf(Table_Users.getValueAt(index,0).toString());
        
        User selected_user = user.getUserById(id);
        
        jSpinner1.setValue(id);
        jTextField_fullname.setText(selected_user.getFullname() );
        jTextField_Username.setText(selected_user.getUsername());
        jTextField_email.setText(selected_user.getEmail());
        jTextField_phone.setText(selected_user.getPhone());
        jPasswordField_1.setText(selected_user.getPassword());
        
        if (selected_user.getUser_type().equals("user"))
        {
            jRadioButton_User.setSelected(true);
        }
        else if(selected_user.getUser_type().equals("admin"))
        {
            jRadioButton_Admin.setSelected(true);
        }
        
    }//GEN-LAST:event_Table_UsersMouseClicked

