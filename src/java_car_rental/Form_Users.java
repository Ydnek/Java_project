    public boolean verify(String add_or_edit)
    {
            String fullname = jTextField_fullname.getText();
            String username = jTextField_Username.getText();
            String phone  = jTextField_phone.getText();
            String email = jTextField_email.getText();
            String passowrd = String.valueOf(jPasswordField_1.getPassword());
            String confirm_passowrd = String.valueOf(jPasswordField_2.getPassword());
           
            
            
        boolean val = false;
        
        if(!fullname.trim().equals("" )  &&  !username.trim().equals("" )  && !phone.trim().equals("" ) 
                && !email.trim().equals("" ) && !passowrd.trim().equals(confirm_passowrd ))
        {
            if("add".equals(add_or_edit))
            {
                if(passowrd.equals(confirm_passowrd))
                {
                     JOptionPane.showMessageDialog(null, "Password Macthed","Valid Info",1);
                    val = true;
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Password Dose not Macth","Invalid Info",2);
                    val = false;
                }
            }
            if("edit".equals(add_or_edit))
            {
                if(passowrd.equals(confirm_passowrd))
                {
                    val = true;
                }
               else
              {
                  JOptionPane.showMessageDialog(null, "Type the Correct Confirmation Password","Invalid Info",2);
                  val = false;
               }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "User Data Cannot Empty","Invalid Info",2);
            val = false;
        }
        return val;
    }   

    
       public void populateJtableWithUsers(){
        
        // clear array list 
        users_List.clear();
        //populate arraylist
        users_List = user.userList();
        // jtabel columns
        
        String [] columnsName = {"ID","Full Name","Username ","User Type","Phone","Email"};        
        
        // jtable rows
        
        Object[][] rows = new Object[users_List .size()][columnsName.length];
        
        for(int i = 0;i < users_List.size();i++){
            
            rows[i][0] = users_List.get(i).getId();
            rows[i][1] = users_List.get(i).getFullname();
            rows[i][2] = users_List.get(i).getUsername();
            rows[i][3] = users_List.get(i).getUser_type();
            rows[i][4] = users_List.get(i).getPhone();
            rows[i][5] = users_List.get(i).getEmail();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        
        Table_Users.setModel(model);
    }
