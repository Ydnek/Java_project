    private void Button_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_addActionPerformed
        // add new brand

       try {

            String fullname = jTextField_fullname.getText();
            String username = jTextField_Username.getText();
            String phone  = jTextField_phone.getText();
            String email = jTextField_email.getText();
            String passowrd = String.valueOf(jPasswordField_1.getPassword());
            String user_type = "user";
            
            if( jRadioButton_Admin.isSelected() ){  user_type = "admin"; }
            
            if(verify("add"))
            {
                user.addUser(fullname, username, passowrd, user_type, phone, email);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Invalid Info",2);
        }

    }//GEN-LAST:event_Button_addActionPerformed

