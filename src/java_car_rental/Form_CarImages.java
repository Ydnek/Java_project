    private void jButton_removeImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeImageActionPerformed
        // Remove image
        // get image by id
        
        try
        {
            int index = Table_Car_image.getSelectedRow();
            int image_id = Integer.valueOf(Table_Car_image.getValueAt(index,0).toString());
            int confirm = JOptionPane.showConfirmDialog(null, "Are You Sure You Want to Delete this Car Image", "Confirm",JOptionPane.YES_NO_OPTION);
            if(confirm==JOptionPane.YES_OPTION)
            {
                car.removeCarImage(image_id);
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Select the Image First","Delete Car Image",2);
        }
        
    }//GEN-LAST:event_jButton_removeImageActionPerformed

    private void jButton_AddImangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddImangeActionPerformed
        //  add car image
                try {
                   // get car id from the jtable
                    int  index = Table_Car.getSelectedRow();
                    int car_id = Integer.valueOf(Table_Car.getValueAt(index, 0).toString());
                    byte[]  image = Files.readAllBytes(Paths.get(jLabel_imagePath.getText()));
                    
                    car.addCarImage(car_id, image);
                    
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Select An Image [ "+ex+" ]","Invalid Info",2);
                }
    }//GEN-LAST:event_jButton_AddImangeActionPerformed

