                     // create a function to  select an image
            // the function will return the image path 
    public String selectImage(){
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select Picture");
        
        fileChooser.setCurrentDirectory(new File("C:\\Users\\fanos\\OneDrive\\Pictures\\CARS"));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("image","png","jpg");
        fileChooser.addChoosableFileFilter(filter);
        
        int state = fileChooser.showSaveDialog(null);
        String path = "";
        
        if(state== JFileChooser.APPROVE_OPTION){
            path = fileChooser.getSelectedFile().getAbsolutePath();
        }
        
        return path;
    }
    
           // create a function to populate the Table 
        public void populateJtableWithCar(){
            // Table columns
        String [] columnsName = {"Id","Model","Class"};        
        
        // jtable rows
        Object[][] rows = new Object[carList.size()] [columnsName.length];
        
        for(int i = 0;i < carList.size();i++){
            
            rows[i][0] = carList.get(i).getId();
            rows[i][1] = carList.get(i).getModel();
            rows[i][2] = carList.get(i).getCalss_();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        
        Table_Car.setModel(model);  
    }
        
                   // create a function to populate the Table 
        public void populateJtableWithCarImage(int car_id)
        {
            ArrayList<Car.CarImage> ImageList = car.carImageList(car_id);
            // Table columns
            String [] columnsName = {"Image ID"};
           
             // jtable rows,
            Object[][] rows = new Object[ImageList.size()] [columnsName.length];

            for(int i = 0;i < ImageList.size();i++)
            {
                rows[i][0] = ImageList.get(i).getImg_id();
            }

            DefaultTableModel model = new DefaultTableModel(rows,columnsName);

            Table_Car_image.setModel(model);  
    }

