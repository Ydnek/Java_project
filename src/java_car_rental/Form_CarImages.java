    private void Table_Car_imageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_Car_imageMouseClicked
        // Display the Selected image
       
         int index = Table_Car_image.getSelectedRow();
        int image_id = Integer.valueOf(Table_Car_image.getValueAt(index,0).toString());
        
        byte[] img = null;
        for(Car.CarImage cimg : carImages)
        {
            if(cimg.getImg_id() == image_id)
            {
                img = cimg.getCar_img();
            }
        }
        
        displayByteImage(jLabel_CarImage.getWidth(),jLabel_CarImage.getHeight(),img,jLabel_CarImage);
    }//GEN-LAST:event_Table_Car_imageMouseClicked

