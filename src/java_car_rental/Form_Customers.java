        // create a function to populate the Table with Customers (id and name)
    public void populateJtableWithCustomers(){
        
        // clear array list 
        customerList.clear();
        //populate arraylist
        customerList = customer.customerList();
        // jtabel columns
        
        String [] columnsName = {"Id","Full Name","Birth Date","Phone","Email","Address"};        
        
        // jtable rows
        
        Object[][] rows = new Object[customerList.size()][columnsName.length];
        
        for(int i = 0;i < customerList.size();i++){
            
            rows[i][0] = customerList.get(i).getId();
            rows[i][1] = customerList.get(i).getFullname();
            rows[i][2] = customerList.get(i).getBirthdate();
            rows[i][3] = customerList.get(i).getPhone();
            rows[i][4] = customerList.get(i).getEmail();
            rows[i][5] = customerList.get(i).getAddress();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        
        Table_Customers.setModel(model);
    }
    

