    private void Table_CustomersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_CustomersMouseClicked
        // get the selected brand

        int index = Table_Customers.getSelectedRow();
        int id = Integer.valueOf(Table_Customers.getValueAt(index,0).toString());
        String fullname = Table_Customers.getValueAt(index,1).toString();
        
        String phone = Table_Customers.getValueAt(index,3).toString();
        String email = Table_Customers.getValueAt(index,4).toString();
        String address = Table_Customers.getValueAt(index,5).toString();
        
        jSpinner1.setValue(id);
        jTextField_fullname.setText(fullname);
        jTextField_phone.setText(phone);
        jTextField_email.setText(email);
        jTextArea_address.setText(address);
        
        Date bdate;        
         try
         {
             bdate = new SimpleDateFormat("yyyy-MM-dd").parse(Table_Customers.getValueAt(index, 2).toString());
             jDateChooser_DOB.setDate(bdate);
         } catch (ParseException ex)
         {
             JOptionPane.showMessageDialog(null, ex.getMessage(),"Invalid Info",2);
         }
      
    }//GEN-LAST:event_Table_CustomersMouseClicked

